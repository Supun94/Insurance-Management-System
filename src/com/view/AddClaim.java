    private void claimAmountTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_claimAmountTextFieldActionPerformed
     
    try {
        double claimAmount=Double.parseDouble(claimAmountTextField.getText());
       Claim claim = ClaimController.searchClaimAmount(policyIdTextField.getText());
       Policy policy = PolicyController.searchPolicy(policyIdTextField.getText());
        if (claim!=null) {
            double policyAmount = policy.getAmount();
            double totalClaim=claim.getClaimAmount();
            if ((totalClaim+Double.parseDouble(claimAmountTextField.getText()))<(policyAmount*0.7)) {
                JOptionPane.showMessageDialog(rootPane, "You can take Rs "+(policyAmount*0.70-(totalClaim+Double.parseDouble(claimAmountTextField.getText())))+" amount.. ");
                generateClaimId();
                descreptionTextArea.requestFocus();
                
                
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Sorry..You reached max Claim Amount\n..You can take only Rs"+(policyAmount*0.70-(totalClaim))+" Claim Amount");
            }
            
        } 
    } catch (SQLException | ClassNotFoundException ex) {
        Logger.getLogger(AddClaim.class.getName()).log(Level.SEVERE, null, ex);
    }

    }//GEN-LAST:event_claimAmountTextFieldActionPerformed

