    public static String generateClaimId() throws SQLException, ClassNotFoundException {
        String lastId = ClaimController.getLastClaimId();

        if (lastId == null) {
            return "CI0001";
        } else {
            int num = Integer.parseInt(lastId.substring(2));
            num++;
            if (num < 10) {
                return "CI000" + num;
            } else if (num < 100) {
                return "CI00" + num;
            } else if (num < 1000) {
                return "CI0" + num;
            } else {
                return "CI" + num;
            }
        }

    }   
